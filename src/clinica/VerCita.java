    public void Reporte(){
    Conection cc= new Conection();
try{
        String rutaInforme=System.getProperty("user.dir")+"/src/Reportes/CitaDoctor.jasper";
        Map parametros= new HashMap();
        parametros.put("ffecha",ffecha.getText());
        parametros.put("coddoc",Integer.parseInt(doc.getText()));
        JasperPrint informe = JasperFillManager.fillReport (rutaInforme,parametros,cc.conexion());
        JasperViewer ventanavisor = new JasperViewer(informe, false);
        ventanavisor.setTitle("Cita del Dia");
        ventanavisor.setVisible(true);
                }
        catch (HeadlessException | JRException  e) {
            System.out.println(e);
            JOptionPane.showMessageDialog(null,"Error en el Reporte","Error",JOptionPane.ERROR_MESSAGE );
        }
}
    
public void mostrarFecha(String valor1){
Conection cc= new Conection();
Connection cn =  cc.conexion();
        DefaultTableModel modelo = new DefaultTableModel();
       
        modelo.addColumn("Fecha-Hora");
        modelo.addColumn("Identidad Paciente");
        modelo.addColumn("Nombre Paciente");
        
        modelo.addColumn("Nombre Doctor");
        
        jTableCliente.setModel(modelo);
        String sql = "";
        if(valor1.equals(""))
        {
            sql = "SELECT cita.fecha,cita.hora,cita.identidad,persona.nombre,persona.apellido,cita.cod_doctor,empleado.nombre,empleado.apellido FROM cita INNER JOIN persona ON cita.identidad=persona.identidad INNER JOIN empleado ON cita.cod_doctor=empleado.cod_empleado";
        }
        else
        {
            
            sql = "SELECT cita.fecha,"
                    + "cita.hora,"
                    + "cita.identidad,"
                    + "persona.nombre,"
                    + "persona.apellido,"
                    + "empleado.nombre,"
                    + "empleado.apellido,"
                    + "cita.cod_doctor  FROM cita INNER JOIN persona ON cita.identidad=persona.identidad INNER JOIN empleado ON cita.cod_doctor=empleado.cod_empleado WHERE fecha='"+valor1+"' AND cod_doctor='"+Integer.parseInt(doc.getText())+"'";
        }
    String []datos = new String [5];
        try {
            Statement st = cn.createStatement();
            ResultSet rs = st.executeQuery(sql);
            while (rs.next()){
                datos [0] = rs.getString(1)+" "+rs.getString(2);
                datos [1] = rs.getString(3);
                datos [2] = rs.getString(4)+" "+rs.getString(5);
                datos [3] = rs.getString(6)+" "+rs.getString(7);
                
                
               
            modelo.addRow(datos);
            }
           // jtb_consulta_venta_auto.setModel(modelo);
        } catch (SQLException ex) {
            Logger.getLogger(BuscarEmpleado.class.getName()).log(Level.SEVERE, null, ex);
        }}
